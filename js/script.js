function navAffixTopSet(){
    var currentWidth = parseInt($(window).width());
    var scrHeight = parseInt($(window).height()) - 50;
    if(currentWidth <= 1000){
        scrHeight = parseInt($('#header-section').height()) - 80;
    }
    $('nav.navbar.navbar-default').attr('data-offset-top',scrHeight);
}
